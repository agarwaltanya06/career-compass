/**
 * Timeline export — turn a route's dated steps into things a student can take
 * away offline: an `.ics` calendar file and a printable (Save-as-PDF) document.
 *
 * Everything here runs in the browser (it touches Blob / document / iframe) and
 * is called from client `onClick` handlers. Dates stay deliberately coarse: the
 * spec only ever gives us a relative `offsetMonths` per step, so calendar events
 * are all-day, pinned to the 1st of the computed month, and every one carries an
 * "approximate — verify" note. Calendars are also capped to the next two years,
 * because a five-year, month-fuzzy reminder is noise, not help.
 */

import type { JourneyStep } from "./types";
import { computeTargetMonth } from "./timeline";

/** Steps further out than this are dropped from the *calendar* (kept in print). */
export const CALENDAR_HORIZON_MONTHS = 24;

/** Localised, presentation-layer strings the builders need. */
export interface ExportLabels {
  /** "Approximate timing — verify exact dates on official sites before acting." */
  approxNote: string;
  /** Marker appended to optional steps, e.g. "Optional". */
  optional: string;
  /** Heading shown above an either/or fork, e.g. "Either / or — pick one". */
  eitherOr: string;
  /** Note added when later steps were trimmed from the calendar. */
  cappedNote: string;
  /** Small footer line, e.g. "Generated by My Career Journey". */
  generatedBy: string;
}

/** Identifies the timeline being exported (for titles, filenames and UIDs). */
export interface ExportContext {
  career: string;
  routeName: string;
  routeId: string;
}

// ---------------------------------------------------------------------------
// .ics calendar
// ---------------------------------------------------------------------------

/** Escape a value for an iCalendar text field (RFC 5545 §3.3.11). */
function icsEscape(text: string): string {
  return text
    .replace(/\\/g, "\\\\")
    .replace(/;/g, "\\;")
    .replace(/,/g, "\\,")
    .replace(/\r?\n/g, "\\n");
}

/** Fold a content line to <=75 octets with CRLF + leading space (RFC 5545 §3.1). */
function foldLine(line: string): string {
  if (line.length <= 75) return line;
  const chunks: string[] = [];
  let rest = line;
  chunks.push(rest.slice(0, 75));
  rest = rest.slice(75);
  while (rest.length > 0) {
    chunks.push(" " + rest.slice(0, 74));
    rest = rest.slice(74);
  }
  return chunks.join("\r\n");
}

/** YYYYMMDD for an all-day DATE value. `month` is 0-indexed. */
function icsDate(year: number, month: number, day: number): string {
  const mm = String(month + 1).padStart(2, "0");
  const dd = String(day).padStart(2, "0");
  return `${year}${mm}${dd}`;
}

/** Current UTC timestamp as YYYYMMDDTHHMMSSZ for DTSTAMP. */
function icsStamp(now = new Date()): string {
  return now.toISOString().replace(/[-:]/g, "").replace(/\.\d{3}/, "");
}

/** True when this step is one side of an either/or fork. */
function inFork(step: JourneyStep, forkIds: Set<string>): boolean {
  return forkIds.has(step.id);
}

/** Collect the ids of every step that participates in a fork. */
function forkStepIds(steps: JourneyStep[]): Set<string> {
  const ids = new Set<string>();
  for (const s of steps) {
    if (s.alternativeTo !== undefined) {
      ids.add(s.id);
      ids.add(s.alternativeTo);
    }
  }
  return ids;
}

/**
 * Build an iCalendar document for a route's steps. Returns the `.ics` text plus
 * whether any steps were trimmed for being beyond the two-year horizon (so the
 * UI can hint at it). Each step becomes one all-day event on the 1st of its
 * computed month, carrying the verify-first note.
 */
export function buildTimelineIcs(
  steps: JourneyStep[],
  anchor: string,
  ctx: ExportContext,
  labels: ExportLabels,
): { ics: string; capped: boolean } {
  const stamp = icsStamp();
  const forkIds = forkStepIds(steps);

  // Calendar gets near-term steps only; print keeps the full plan.
  const withinHorizon = steps.filter((s) => s.offsetMonths <= CALENDAR_HORIZON_MONTHS);
  const capped = withinHorizon.length < steps.length;

  const calDesc = capped ? `${labels.approxNote} ${labels.cappedNote}` : labels.approxNote;

  const lines: string[] = [
    "BEGIN:VCALENDAR",
    "VERSION:2.0",
    "PRODID:-//My Career Journey//Timeline//EN",
    "CALSCALE:GREGORIAN",
    "METHOD:PUBLISH",
    `X-WR-CALNAME:${icsEscape(`${ctx.career} — ${ctx.routeName}`)}`,
    `X-WR-CALDESC:${icsEscape(calDesc)}`,
  ];

  for (const step of withinHorizon) {
    const { year, month } = computeTargetMonth(anchor, step.offsetMonths);
    const start = icsDate(year, month, 1);
    const end = icsDate(year, month, 2); // DTEND is exclusive for all-day events.

    let summary = step.title;
    if (step.optional) summary += ` (${labels.optional})`;

    const descParts = [step.description];
    if (inFork(step, forkIds)) descParts.push(labels.eitherOr);
    descParts.push(labels.approxNote);
    const description = descParts.join("\n\n");

    lines.push(
      "BEGIN:VEVENT",
      `UID:${ctx.routeId}-${step.id}-${stamp}@career-journey`,
      `DTSTAMP:${stamp}`,
      `DTSTART;VALUE=DATE:${start}`,
      `DTEND;VALUE=DATE:${end}`,
      `SUMMARY:${icsEscape(summary)}`,
      `DESCRIPTION:${icsEscape(description)}`,
      "TRANSP:TRANSPARENT",
      "END:VEVENT",
    );
  }

  lines.push("END:VCALENDAR");

  // Fold every line and join with CRLF, as required by RFC 5545.
  const ics = lines.map(foldLine).join("\r\n") + "\r\n";
  return { ics, capped };
}

