/** A single step rendered for the printable timeline. */
export interface PrintStep {
  /** Coarse period label, e.g. "Mid 2026". */
  period: string;
  title: string;
  type: string;
  description: string;
  optional: boolean;
  /** First step of a fork → render the "either/or" wrapper opens here. */
  forkStart: boolean;
  /** Subsequent fork alternative (a sibling within the same fork). */
  forkMember: boolean;
}

/** Build a printable HTML document for a route's full (uncapped) timeline. */
export function buildTimelinePrintHtml(
  printSteps: PrintStep[],
  ctx: ExportContext,
  labels: ExportLabels,
  disclaimers: string[],
  headings: { forCareer: string; timeline: string; important: string },
  /** When set, a prominent AI-generated/unverified banner is printed at the top. */
  unverifiedBanner?: { title: string; body: string },
): string {
  const stepsHtml = printSteps
    .map((s) => {
      const optionalTag = s.optional
        ? `<span class="tag">${htmlEscape(labels.optional)}</span>`
        : "";
      const forkHead = s.forkStart
        ? `<div class="fork-head">${htmlEscape(labels.eitherOr)}</div>`
        : "";
      const inner = `${forkHead}<div class="title">${htmlEscape(s.title)}${optionalTag}</div>
        <div class="type">${htmlEscape(s.type)}</div>
        <p class="desc">${htmlEscape(s.description)}</p>`;
      const body = s.forkStart || s.forkMember
        ? `<div class="fork">${inner}</div>`
        : inner;
      return `<li><div class="period">${htmlEscape(s.period)}</div><div class="body">${body}</div></li>`;
    })
    .join("\n");

  const disclaimersHtml =
    disclaimers.length > 0
      ? `<div class="disclaimer"><strong>${htmlEscape(headings.important)}</strong>
         <ul>${disclaimers.map((d) => `<li>${htmlEscape(d)}</li>`).join("")}</ul></div>`
      : "";

  const bannerHtml = unverifiedBanner
    ? `<div class="unverified"><div class="uv-title">⚠️ ${htmlEscape(unverifiedBanner.title)}</div><div class="uv-body">${htmlEscape(unverifiedBanner.body)}</div></div>`
    : "";

  const body = `
  ${bannerHtml}
  <p class="sub">${htmlEscape(headings.forCareer)}</p>
  <h1>${htmlEscape(ctx.career)}</h1>
  <h2>${htmlEscape(ctx.routeName)} — ${htmlEscape(headings.timeline)}</h2>
  <p class="note">${htmlEscape(labels.approxNote)}</p>
  <ol class="steps">${stepsHtml}</ol>
  ${disclaimersHtml}
  <footer>${htmlEscape(labels.generatedBy)}</footer>`;

  return printShell(`${ctx.career} — ${ctx.routeName}`, body);
}

/** A blank-line plan section for the "plan it yourself" printable. */
export interface PrintPlanSection {
  heading: string;
  intro?: string;
  /** Each becomes a labelled blank line to write on. */
  blanks?: string[];
  /**
   * Labelled entries that carry whatever the user typed: a filled value is
   * printed as text; an empty one falls back to a blank line to write on. This
   * is how the download/print reflects the values already entered on the page.
   */
  fields?: { label: string; value: string }[];
  /** Each becomes a plain numbered list item (the research guide). */
  items?: string[];
}

/** Build a printable, fill-in-on-paper version of the "plan it yourself" page. */
export function buildPlanPrintHtml(
  title: string,
  intro: string,
  sections: PrintPlanSection[],
  generatedBy: string,
): string {
  const sectionsHtml = sections
    .map((sec) => {
      const introHtml = sec.intro ? `<p class="note">${htmlEscape(sec.intro)}</p>` : "";
      let listHtml = "";
      if (sec.fields) {
        // A typed value prints as text; an empty field becomes a blank line.
        listHtml = `<ol class="steps">${sec.fields
          .map((f) => {
            const value = f.value.trim();
            const filled = value
              ? `<p class="desc">${htmlEscape(value)}</p>`
              : `<span class="line"></span>`;
            return `<li><div class="body"><div class="title">${htmlEscape(
              f.label,
            )}</div>${filled}</div></li>`;
          })
          .join("")}</ol>`;
      } else if (sec.blanks) {
        listHtml = `<ol class="steps">${sec.blanks
          .map(
            (b) =>
              `<li><div class="body"><div class="title">${htmlEscape(
                b,
              )}</div><span class="line"></span></div></li>`,
          )
          .join("")}</ol>`;
      } else if (sec.items) {
        listHtml = `<ol style="padding-left:18px;color:#44403c;font-size:13px;">${sec.items
          .map((i) => `<li style="margin:4px 0;">${htmlEscape(i)}</li>`)
          .join("")}</ol>`;
      }
      return `<h2>${htmlEscape(sec.heading)}</h2>${introHtml}${listHtml}`;
    })
    .join("\n");

  const body = `
  <h1>${htmlEscape(title)}</h1>
  <p class="note">${htmlEscape(intro)}</p>
  ${sectionsHtml}
  <footer>${htmlEscape(generatedBy)}</footer>`;

  return printShell(title, body);
}
